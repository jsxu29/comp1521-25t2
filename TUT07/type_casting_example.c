#include <stdio.h>
#include <stdint.h>

int main() {

    int16_t i = -1000;
    // 1000 - 0b 00000011 11101000
    // -x = ~x + 1
    //        0b 11111100 00010111
    //        0b 11111100 00011000

    uint32_t j = i;
    // 0b ________ ________ 11111100 00011000
    // 0b 11111111 11111111 11111100 00011000
    // 0b11111111111111111111110000011000

    printf("i: %d\nj: %u\n", i, j);
    return 0;
}
